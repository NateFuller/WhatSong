import SwiftUI

extension Color {
    enum Background {
        static let error = Color("bg.error")
        static let input = Color("bg.input")
        static let page = Color("bg.page")
    }
    
    enum Text {
        static let input = Color("text.input")
        static let primary = Color("text.primary")
        static let placeholder = Color("text.placeholder")
    }
}
